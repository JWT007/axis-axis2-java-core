package org.apache.savan.configuration;

public class SubscriberBean {
	
	String name;
	String clazz;
	
	public String getClazz() {
		return clazz;
	}
	public String getName() {
		return name;
	}

	public void setClazz(String clazz) {
		this.clazz = clazz;
	}
	public void setName(String name) {
		this.name = name;
	}
	
	
}
